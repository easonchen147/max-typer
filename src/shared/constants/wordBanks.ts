interface WordBankDefinition {
  id: WordBankId
  label: string
  description: string
  words: Record<PracticeDifficulty, string[]>
  phrases: string[]
}

const createEntries = (
  bankId: WordBankId,
  difficulty: PracticeDifficulty,
  values: string[],
): WordEntry[] =>
  values.map((value) => ({
    value,
    difficulty,
    tags: [bankId],
  }))

