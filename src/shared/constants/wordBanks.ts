export const WORD_BANK_OPTIONS = Object.values(WORD_BANKS).map(({ id, label, description }) => ({
  id,
  label,
  description,
}))

export const getWordBankEntries = (bankId: WordBankId): WordEntry[] => {
  const bank = WORD_BANKS[bankId]

  return [
    ...createEntries(bankId, 'starter', bank.words.starter),
    ...createEntries(bankId, 'standard', bank.words.standard),
    ...createEntries(bankId, 'challenge', bank.words.challenge),
  ]
}

export const getWordBankPhrases = (bankId: WordBankId) => [...WORD_BANKS[bankId].phrases]
